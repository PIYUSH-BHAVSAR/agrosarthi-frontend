// Function to update the language content
function updateLanguage(language) {
    // Check if the selected language exists in the translations
    if (!translations[language]) {
        console.error(`Language '${language}' is not available.`);
        return;
    }

    // Find all elements with the 'data-translate' attribute
    const translateElements = document.querySelectorAll('[data-translate]');

    // Loop through all elements and update their text content
    translateElements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[language] && translations[language][key]) {
            element.textContent = translations[language][key];
        }
    });
}

// Event listener for language change
document.getElementById('language-select').addEventListener('change', function() {
    const selectedLanguage = this.value;
    updateLanguage(selectedLanguage);
});

// Set default language to English
document.addEventListener('DOMContentLoaded', function() {
    updateLanguage('en');  // Set default language on page load
});

